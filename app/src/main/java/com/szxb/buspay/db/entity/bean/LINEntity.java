public class LINEntity {

    /**
     * l : 0188
     * n : 123123
     * city : 0
     * bank : 0
     */

    private String l;
    private String n;
    private String city;
    private String bank;

    public String getL() {
        return l;
    }

    public void setL(String l) {
        this.l = l;
    }

    public String getN() {
        return n;
    }

    public void setN(String n) {
        this.n = n;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getBank() {
        return bank;
    }

    public void setBank(String bank) {
        this.bank = bank;
    }
}
